x = 10
y = 10

lebih_besar = x > y
lebih_besar_sama = x >= y
lebih_kecil = x < y
lebih_kecil_sama = x <= y
sama_dengan = x == y
tidak_sama_dengan = x != y


print(f"apakah {x} besar(>) dari {y} : {lebih_besar}")
print(f"apakah {x} besar sama dengan(>=) dari {y} : {lebih_besar_sama}")

print(f"apakah {x} kecil(<) dari {y} : {lebih_kecil}")
print(f"apakah {x} kecil sama dengan(<=) dari {y} : {lebih_besar_sama}")
print(f"apakah {x} tidak sama dengan(!=) {y} : {tidak_sama_dengan}")