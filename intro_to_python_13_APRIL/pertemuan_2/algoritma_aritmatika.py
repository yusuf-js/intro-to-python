x = 5
y = 2

#penjumlahan
hasil = x + y

#pengurangan
hasil = x - y

#perkalian
hasil = x * y

#pembagian
hasil = x / y

#coversi
hasil_conversi = int(hasil)

#cara cepat
hasil_cepat = 5//2

#modulus (%)
#modulus = sisa dari pembagian
hasil = x % y

#pangkat
hasil = x ** y

#cara simpel
x = x + 1

#incerment
x += 1

print(f"Hasil Penjumlahan {x} + {y} = {hasil}")
print(f"Hasil Penjumlahan {x} - {y} = {hasil}")
print(f"Hasil Penjumlahan {x} * {y} = {hasil}")
print(f"Hasil konversi int {x} / {y} = {hasil}")
print(f"Hasil konversi {x} / {y} = {hasil_conversi}")
print(f"hasil dari konversi int {x} // {y} = {hasil_cepat}")
print(f"hasil dari modulus {x} % {y} = {hasil}")
print(f"hasil dari pangkat {x} ** {y} = {hasil}")
print(f"hasil = {x}")
